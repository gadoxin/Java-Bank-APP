    private void btnWithdrawActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnWithdrawActionPerformed
        double amount = Double.parseDouble(txtEnteredAmount.getText());
        double balance = Double.parseDouble(lblBalance.getText());
        if(amount<=balance){
            //reduce the balance by amount
            double newBalance = balance-=amount;
            String query = "UPDATE user SET balance='"+newBalance+"' WHERE account_number='"+accno+"'";
        
        try {
            pst = con.prepareStatement(query);
            pst.execute();
            
            //clearTextBoxes();
            txtEnteredAmount.setText("");
            
            JFrame f = new JFrame();
            JOptionPane.showMessageDialog(f, "Balance Updated Successfully");
            lblBalance.setText(Double.toString(newBalance));
        } catch (SQLException ex) {
            JFrame f = new JFrame();
            JOptionPane.showMessageDialog(f, ex);
        }
        }else{
            JFrame f = new JFrame();
            JOptionPane.showMessageDialog(f, "Balance is less than amount");
        }
    }//GEN-LAST:event_btnWithdrawActionPerformed

