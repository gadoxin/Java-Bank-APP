    private void getDataValues() {
        String query = "SELECT name, account, balance FROM user WHERE account_number = '"+accno+"'";
        try{
            pst = con.prepareStatement(query);
            rs = pst.executeQuery();
            //tblEmployees.setModel(DbUtils.resultSetToTableModel(rs));
                if (rs.next()) {  
                String s = rs.getString(1);  
                String s1 = rs.getString(2);  
                String s2 = rs.getString(3);  
                //String s3 = rs.getString(4);  
                //Sets Records in TextFields.
                
                lblAccNo.setText(Integer.toString(accno));  
                lblName.setText(s);  
                lblAccountType.setText(s1);  
                lblBalance.setText(s2);  
            } else {  
                JOptionPane.showMessageDialog(null, "Name not Found");  
            }
        }
        catch(SQLException e){
            JFrame f = new JFrame();
            JOptionPane.showMessageDialog(f, e);
        }
       }
